public class Hodovaya {
    private int radius;
    private int number;

    public Hodovaya(int radius, int number) {
        this.radius = radius;
        this.number = number;
    }

    @Override
    public String toString() {
        return "Hodovaya{" +
                "radius=" + radius +
                ", number=" + number +
                '}';
    }
}
